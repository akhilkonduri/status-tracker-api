package gov.sam.status.tracker.pojo;

public class ContractOpps {

	private String noticeId;
	private String solicitationNumber;
	private String possibleContractName;
	private String naicsCode;
	private String department;
	private String postedDate;
	private String active;

	public String getNoticeId() {
		return noticeId;
	}

	public void setNoticeId(String noticeId) {
		this.noticeId = noticeId;
	}

	public String getSolicitationNumber() {
		return solicitationNumber;
	}

	public void setSolicitationNumber(String solicitationNumber) {
		this.solicitationNumber = solicitationNumber;
	}

	public String getPossibleContractName() {
		return possibleContractName;
	}

	public void setPossibleContractName(String possibleContractName) {
		this.possibleContractName = possibleContractName;
	}

	public String getNaicsCode() {
		return naicsCode;
	}

	public void setNaicsCode(String naicsCode) {
		this.naicsCode = naicsCode;
	}

	public String getDepartment() {
		return department;
	}

	public void setDepartment(String department) {
		this.department = department;
	}

	public String getPostedDate() {
		return postedDate;
	}

	public void setPostedDate(String postedDate) {
		this.postedDate = postedDate;
	}

	public String getActive() {
		return active;
	}

	public void setActive(String active) {
		this.active = active;
	}

}